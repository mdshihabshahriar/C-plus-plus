#include<iostream>
using namespace std;
int main()
{
    long long X,Y;
    cin >> X >> Y;

    // long long summation = X + Y;
    // long long multipication = X * Y;
    // int substraction = X - Y;
    cout << X << " + " << Y << " = " << X+Y << endl;
    cout << X << " * " << Y << " = " << X*Y << endl;
    cout << X << " - " << Y << " = " << X-Y << endl;
    return 0;
}